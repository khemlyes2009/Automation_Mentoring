package basic;

public class LearEscapeSequence {
	
	// Escape sequence 
	
	public static void main(String[] args) {
		
		System.out.println("This is QA Engineering class");
		System.out.println("This is QA \tEngineering class");
		System.out.println("This is QA Engineering \nclass");
		System.out.println("This is QA \'Engineering\' class");
		System.out.println("This is QA \"Engineering\" class");
		System.out.println("This is QA \\Engineering\\ class");

		
		
		
		
		
	}
	
	
	
	
	

}
